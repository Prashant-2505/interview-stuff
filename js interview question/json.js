// What is JSON and its common operations

// jSON stands for JavaScript Object Notation. It is a lightweight data-interchange format that is both human and machine-readable. JSON is often used to transmit data between a server and web application, as well as for storing and exchanging data.

// JSON is a text-based format that is made up of key-value pairs. The keys are always strings, and the values can be strings, numbers, booleans, arrays, or objects. JSON objects are enclosed in curly braces, and arrays are enclosed in square brackets.



