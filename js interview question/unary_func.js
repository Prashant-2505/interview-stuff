// What is a unary function in js

// a unary function is a function that takes exactly one argument. The term "unary" comes from the Latin word "unarius," meaning "consisting of one." Unary functions are designed to operate on a single value and perform some computation or transformation.

// Unary function that doubles a number
function double(number) {
    return number * 2;
  }
  
  console.log(double(5)); // Output: 10
  